    ticket,
    order_id,
    login,
    symbol,
    side,
    volume,
    volume_label,
    open_time,
    open_time_jst,
    close_time,
    close_time_jst,
    hold_time,
    hold_label,
    open_price,
    close_price,
    profit,
    swaps,
    commission,
    reason,
    flag,
    client_type,
    business_type,
    platform,
    tick_1s_label,
    tick_1s_rate,
    tick_5s_label,
    tick_5s_rate,
    tick_10s_label,
